public class CodeforString {
    public static void main(String[] args) {
       String myName = "My Name is Manish";
       String myAge = "My Age is 27 years";
       String myPlace ="I am form India";
       System.out.println(myAge);
       System.out.println(myName);
       System.out.println(myPlace);
       
       String myInfo = myName +myAge +myPlace;
       System.out.println(myInfo);
       System.out.println(myName +" " +myAge +" "+ myPlace);
       
       int myInt =100;
       myAge =myAge+myInt;
       System.out.println(myAge);
       String myInt1 ="100";
       String myInt2 ="200";
       int myInt3 =300;
       double myDouble = 543.1234;
       System.out.println(myInt1+myInt2+myInt3+myDouble);
    }
       
    }