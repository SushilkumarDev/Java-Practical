public class Switchchallenge {

    public static void main(String[] args) {
        char myLetter = 'p';

        switch (Character.toUpperCase(myLetter)) {
            case 'A':
                System.out.println("It is a vowel");
                break;

            case 'E':
                System.out.println("It is a vowel");
                break;

            case 'I':
                System.out.println("It is a vowel");
                break;
            case 'O':
                System.out.println("It is a vowel");
                break;

            case 'U':
                System.out.println("It is a vowel");
                break;

            default:
                System.out.println("Oops! Not a Vowel");
        }
    }
}

